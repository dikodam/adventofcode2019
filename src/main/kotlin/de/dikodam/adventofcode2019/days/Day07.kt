fun runAmplifierSequence(intCode:IntArray, phaseSequence: List<Int>): Int {
    val ampA = IntcodeComputer(intCode)
    val ampB = IntcodeComputer(intCode)
    val ampC = IntcodeComputer(intCode)
    val ampD = IntcodeComputer(intCode)
    val ampE = IntcodeComputer(intCode)

    val bInput = ampA.run(listOf(phaseSequence[0], 0)).last()
    val cInput = ampB.run(listOf(phaseSequence[1], bInput)).last()
    val dInput = ampC.run(listOf(phaseSequence[2], cInput)).last()
    val eInput = ampD.run(listOf(phaseSequence[3], dInput)).last()
    return ampE.run(listOf(phaseSequence[4], eInput)).last()
}

